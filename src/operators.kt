fun main(){
    val a = 45
    val b = 23
    print("$a + $b = ")
    println(a + b)
    println(a == b)

    print("$a - $b = ")
    println(a - b)
    println(a > b)

    print("$a * $b = ")
    println(a * b)
    println(a != b)

    print("$a / $b = ")
    println(a / b)
    println(a < b)

    print("$a % $b = ")
    println(a % b)
    println(a >= b)
}